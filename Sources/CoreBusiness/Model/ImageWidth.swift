import Foundation

/// An width supported for image resizing.
public enum ImageWidth: Int {
    // 240 pixels.
    case width240 = 240

    // 320 pixels.
    case width320 = 320

    // 480 pixels.
    case width480 = 480

    // 720 pixels.
    case width720 = 720

    // 960 pixels.
    case width960 = 960

    // 1920 pixels.
    case width1920 = 1920
}
